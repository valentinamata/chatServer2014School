    QuizzGame(String gameName, String name1, String name2) {
        
        this();
        this.name1= name1;
        this.name2=name2;
        quizz= new Controller();
        scoreFile="Score.txt";
        oldScore=null;
        
        quizz.load(scoreFile);
        this.gameName=gameName;
        if(name2.equals("computer")){
            Player2.setText("computer");
            Player1.setText(name1);
            score1= quizz.lookup(name1);
            System.out.println(name1);
            System.out.println(score1);
            prevScore1=10;
            ScoreField2.setText("Good Luck!");
            
        }
        else if(name2.equals("inFolder"))
        {
            Player2.setText("computer");
            Player1.setText(name1);
            score1=quizz.lookup(name1);
            prevScore1=Integer.parseInt(score1);
            ScoreField2.setText("Good Luck!");
            oldScore = name1+","+score1;
        }
        else{
            Player1.setText(name1);
            Player2.setText(name2);
            scoreFile="friends.txt";
            
            prevScore1=0;
            prevScore2=0;
        }
            tami1=null;
            tami2=null;
        guessB=0;
        
        
        quizz.load(this.gameName+".txt");
        jGameName.setText(this.gameName);
        line=this.gameName;
        //changes image according to the quizz you choose to play
        Image.setIcon(new javax.swing.ImageIcon("C:\\Users\\Valentina\\Documents\\NetBeansProjects\\Flow3big\\src\\"+this.gameName+".jpg"));
       
        System.out.println(this.gameName);
    }

     
     

        
    
