    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //method used in the submit button which will save to the file and delete the previous entry the person had, while adding the new score
        if(name2.equals("computer")){
             quizz.loadQuizz(scoreFile);
           quizz.add(name1+","+tami1);
           quizz.save(scoreFile);
        }
        else if (name2.equals("inFolder")){
       
        String[] sta = quizz.checkTitle(oldScore).split(",");
        if(name1.equals(sta[0]))
        {
            quizz.loadQuizz(scoreFile);
            quizz.delete(oldScore);
            oldScore=name1+","+tami1;
        quizz.add(oldScore);
        quizz.save(scoreFile);
            
        }
        }
        else{
            
           quizz.add(name1+","+tami1);
           quizz.save(scoreFile);
           quizz.add(name2+","+ tami2);
           quizz.save(scoreFile);
            
        
        }
        
         java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                new PlayerScore(scoreFile).setVisible(true);
            }
        });
         this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

