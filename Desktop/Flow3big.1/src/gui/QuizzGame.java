    private void GuessButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuessButtonActionPerformed
        // TODO add your handling code here:
        //when this button is clicked you get to add score to your previous score
        guessB++;
        if(jQuestion.getText().equals("") || jAnswer.getText().equals(""))
        {
            
             InputStream in;
        try{
        in = new FileInputStream(new File ("C:\\Users\\Valentina\\Documents\\NetBeansProjects\\Flow3big\\src\\button-12.wav"));
            AudioStream audioS= new AudioStream(in);
            AudioPlayer.player.start(audioS);
           
            JOptionPane.showMessageDialog(this, "Both words must be completed.",
            "bothWord", JOptionPane.INFORMATION_MESSAGE);
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);}
           
        }
        else if(quizz.checkGuess(jQuestion.getText(), jAnswer.getText()))
        { if(name2.equals("computer") || name2.equals("inFolder")){
            
            
            prevScore1 += quizz.scores();
            
            tami1 = Integer.toString(prevScore1);
            
            ScoreField1.setText(tami1);
            
            jQuestion.setText("");
            jAnswer.setText("");
            }
        else{
           if(guessB%2 != 0){
            
            prevScore1 += 5;
            
            tami1 = Integer.toString(prevScore1);
            
            ScoreField1.setText(tami1);
            jQuestion.setText("");
            jAnswer.setText("");
}
        
        else {
           prevScore2 += 5;
            
            tami2 = Integer.toString(prevScore2);
            
            ScoreField2.setText(tami2);
            jQuestion.setText("");
            jAnswer.setText("");
        }
        }
        }
        else {
            jQuestion.setText("");
            jAnswer.setText("");
        }
        
    }//GEN-LAST:event_GuessButtonActionPerformed

